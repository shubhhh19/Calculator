def add(a, b):
    result = a + b
    print(str(a) + " + " + str(b) + " = " + str(result))
    
def sub(a, b):
    result = a - b
    print(str(a) + " - " + str(b) + " = " + str(result))

def mul(a, b):
    result = a * b
    print(str(a) + " * " + str(b) + " = " + str(result))

def div(a, b):
    result = a / b
    print(str(a) + " / " + str(b) + " = " + str(result))

